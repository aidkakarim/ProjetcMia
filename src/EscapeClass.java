public class EscapeClass {

    public static void main(String[] args) {


        // \t inserts a tab in the text at this point

        System.out.println("Java\tis\tFun");

        // \b deletes a space in the text at this point

        System.out.println("java \bis Fun 02");

        // \n inserts a newline in the text at this point

        System.out.println("Welcome \nto Java");

        // \r inserts a carriage
        System.out.println("Hello \rand Welcome");

        // \f inserts a form feed
        System.out.println("Java is \ffunny");

        //



    }
}
